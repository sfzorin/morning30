package content

// Voice holds the short non-blocking cues spoken during an exercise.
type Voice struct {
	Start  string   // at the start
	Mid    []string // 1–2 technique cues during the work
	Last   string   // ~5 s before a timed exercise ends ("" → engine default)
	Finish string   // at the end
}

// Detail is the rich, on-demand exercise content (screen card + voice). Stored
// in Russian (canonical); short on-screen cue/warning are multilingual via i18n.
type Detail struct {
	Desc      string
	HowTo     []string
	Correct   []string
	Wrong     []string
	Breathing string
	Safety    string
	Voice     Voice
}

// GetDetail returns the Russian detailed content for an exercise (used by export).
func GetDetail(id string) (Detail, bool) {
	d, ok := Details[id]
	return d, ok
}

// detailsByLang holds the rich content per language. Currently only English is
// authored for the new exercise set; every other language falls back to English.
var detailsByLang = map[string]map[string]Detail{
	"en": detailsEN,
	"ru": detailsRU,
	"tr": detailsTR,
	"de": detailsDE,
	"es": detailsES,
	"fr": detailsFR,
	"it": detailsIT,
}

// HasDetailLang reports whether per-exercise content/voice exists for a language
// (others use the generic localized voice cues + English card fallback).
func HasDetailLang(lang string) bool {
	_, ok := detailsByLang[lang]
	return ok
}

// GetDetailL returns the detailed content in the given language, falling back to
// English, then Russian.
func GetDetailL(lang, id string) (Detail, bool) {
	if m, ok := detailsByLang[lang]; ok {
		if d, ok := m[id]; ok {
			return d, true
		}
	}
	if d, ok := detailsEN[id]; ok {
		return d, true
	}
	d, ok := Details[id]
	return d, ok
}

